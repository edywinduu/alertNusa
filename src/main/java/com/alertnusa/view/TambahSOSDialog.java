    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        String nama = txtNamaInstansi.getText().trim();
        String nomor = txtNomorTelepon.getText().trim();
        String wilayah = txtWilayah.getText().trim();

        // Validasi input kosong
        if (nama.isEmpty() || nomor.isEmpty() || wilayah.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Semua kolom input wajib diisi!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        String query = "INSERT INTO sos_contacts (institution_name, phone_number, region) VALUES (?, ?, ?)";

        try (Connection conn = DatabaseConnection.getConnection(); PreparedStatement ps = conn.prepareStatement(query)) {

            ps.setString(1, nama);
            ps.setString(2, nomor);
            ps.setString(3, wilayah);

            int hasil = ps.executeUpdate();
            if (hasil > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Kontak darurat baru berhasil ditambahkan!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);

                // Segarkan layar halaman SOS admin secara live lewat jembatan public
                if (parentPanel != null) {
                    parentPanel.refreshHalamanSOS();
                }
                this.dispose(); // Tutup dialog
            }
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan ke database: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanMouseClicked

